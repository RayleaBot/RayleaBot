#!/usr/bin/env node
import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')

const schemas = [
  {
    constName: 'ConfigUserSchemaJSON',
    source: 'contracts/config.user.schema.json',
  },
  {
    constName: 'PluginInfoSchemaJSON',
    source: 'contracts/plugin-info.schema.json',
  },
]

function quoteBytes(buffer) {
  const chunkSize = 16
  const lines = []
  for (let offset = 0; offset < buffer.length; offset += chunkSize) {
    const chunk = [...buffer.subarray(offset, offset + chunkSize)]
    lines.push(`\t${chunk.map((value) => `0x${value.toString(16).padStart(2, '0')}`).join(', ')},`)
  }
  return lines.join('\n')
}

const output = []
output.push('// Code generated by scripts/generate-runtime-schemas.mjs; DO NOT EDIT.')
output.push('package schemaassets')
output.push('')

for (const schema of schemas) {
  const schemaPath = path.join(repoRoot, schema.source)
  const bytes = normalizeSchemaBytes(await fs.readFile(schemaPath))
  output.push(`// ${schema.constName} is generated from ${schema.source}.`)
  output.push(`var ${schema.constName} = []byte{`)
  output.push(quoteBytes(bytes))
  output.push('}')
  output.push('')
}

if (output.at(-1) === '') {
  output.pop()
}

const outputPath = path.join(repoRoot, 'server/internal/schemaassets/assets_gen.go')
await fs.mkdir(path.dirname(outputPath), { recursive: true })
await fs.writeFile(outputPath, `${output.join('\n')}\n`, 'utf8')

function normalizeSchemaBytes(buffer) {
  return Buffer.from(buffer.toString('utf8').replace(/\r\n?/g, '\n'), 'utf8')
}
